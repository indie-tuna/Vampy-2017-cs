import turtle as t
def square(size):
	t.forward(size)
	t.right(90)
	t.forward(size)
	t.right(90)
	t.forward(size)
	t.right(90)
	t.forward(size)
	t.right(90)
square(100)
t.clear()
square(75)
t.clear()
def triangle(size):
	t.right(60)
	t.forward(size)
	t.right(120)
	t.forward(size)
	t.right(120)
	t.forward(size)
triangle(100)
t.clear()
triangle(75)
t.clear()
